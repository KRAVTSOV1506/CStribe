<script>
  import Web3 from "web3";
  import Web3Modal from "web3modal";
  import WalletConnectProvider from "@walletconnect/web3-provider";
  import MPlanets from '../contracts/MPlanets.json';

  const providerOptions = {
    walletconnect: {
        package: WalletConnectProvider,
        options: {
          rpc: {
            // Add infura for mainnet
            1: ""
          },
          infuraId: ""
        }
    },

  }

  const web3modal = new Web3Modal({
    network: "mainnet",
    providerOptions: providerOptions
  })

  export default {
    name: 'Home',
    data() {
      return {
        amount_of_nfts: 1
      }
    },
    created() {
    },
    mounted() {
      this.rangeUpdater()
    },
    methods: {
      async connectWallet() {
        try {
          const provider = await web3modal.connect()
          this.web3 = new Web3(provider)
        } catch (error) {
          console.log(error)
        }
      },
      async estimateGas(acc, contractAddr, data, value, stateGas) {
        var amountBN = new this.web3.utils.BN()
        amountBN = new this.web3.utils.BN( await this.web3.eth.estimateGas({
          from: acc, 
          data: data,
          to: contractAddr,
          value: value
        }) )
        return amountBN.add(new this.web3.utils.BN(stateGas))
      },
      async mintNFT() {
        try {
          await this.connectWallet()
          const accounts = await this.web3.eth.getAccounts()
          const accountAddress = accounts[0]
          const networkId = await this.web3.eth.net.getId()
          

          this.PlanetsInstance = new this.web3.eth.Contract(
            MPlanets.abi,
            MPlanets.networks[networkId] &&  MPlanets.networks[networkId].address,
          )

          const data = "0x52850f43" + ethers.utils.hexZeroPad(this.amount_of_nfts, 32).substring(2)

          const amount = await this.PlanetsInstance.methods.getNFTPrice().call()
          const estimatedGas = await this.estimateGas(accountAddress, MPlanets.networks[networkId].address, data, 0, "5000")
          const gasPrice = await this.web3.eth.GetGasPrice()

          await this.PlanetsInstance.methods.mintPlanets(this.amount_of_nfts).send({
            from: accountAddress, 
            value: amount,
            gasLimit: estimatedGas,
            gasPrice: gasPrice
            }).on('transactionHash', (error) => {
              alert("Thank you for the claiming! The transaction will proceed in minutes. You can check your NFT on OpenSea then!")
            }
          )
        } catch (error) {
          alert(error)
        }
      },
      rangeUpdater() {
        const range = document.getElementById("range");
        
        // https://stackoverflow.com/questions/10756313/javascript-jquery-map-a-range-of-numbers-to-another-range-of-numbers
        const scale = (num, in_min, in_max, out_min, out_max) => {
          return ((num - in_min) * (out_max - out_min)) / (in_max - in_min) + out_min;
        };

        range.addEventListener("input", (e) => {
          const value = +e.target.value;
          this.amount_of_nfts = value
          const label = e.target.nextElementSibling;
          const rangeWidth = getComputedStyle(e.target).getPropertyValue("width");
          const labelWidth = getComputedStyle(label).getPropertyValue("width");
          // remove px
          const numWidth = +rangeWidth.substring(0, rangeWidth.length - 2);
          const numLabelWidth = +labelWidth.substring(0, labelWidth.length - 2);
          const max = +e.target.max;
          const min = +e.target.min;
          const left =
            value * (numWidth / max) -
            numLabelWidth / 2 +
            scale(value, min, max, 10, -10);
          label.style.left = `${left}px`;
          label.innerHTML = value;
        });


      }
    }
  }
</script>



